from flask import Flask, request, render_template, jsonify
import joblib
import numpy as np
import os

app = Flask(__name__)

# load model

MODEL_PATH = os.path.join('models', 'model.pkl')
model = None
if os.path.exists(MODEL_PATH):
    model = joblib.load(MODEL_PATH)

# default feature names in the dataset (exclude 'name' and 'status')
DEFAULT_FEATURE_NAMES = [
    'MDVP:Fo(Hz)', 'MDVP:Fhi(Hz)', 'MDVP:Flo(Hz)', 'MDVP:Jitter(%)', 'MDVP:Jitter(Abs)',
    'MDVP:RAP', 'MDVP:PPQ', 'Jitter:DDP', 'MDVP:Shimmer', 'MDVP:Shimmer(dB)',
    'Shimmer:APQ3', 'Shimmer:APQ5', 'MDVP:APQ', 'Shimmer:DDA', 'NHR', 'HNR',
    'RPDE', 'DFA', 'spread1', 'spread2', 'D2', 'PPE'
]

# If a top_features.json file exists (generated by analysis), use it to limit inputs
TOP_FEATURES_FILE = 'top_features.json'
if os.path.exists(TOP_FEATURES_FILE):
    try:
        import json
        with open(TOP_FEATURES_FILE, 'r', encoding='utf-8') as f:
            tf = json.load(f)
        # validate
        if isinstance(tf, list) and len(tf) > 0:
            FEATURE_NAMES = tf
        else:
            FEATURE_NAMES = DEFAULT_FEATURE_NAMES
    except Exception:
        FEATURE_NAMES = DEFAULT_FEATURE_NAMES
else:
    FEATURE_NAMES = DEFAULT_FEATURE_NAMES


@app.route('/')
def index():
    return render_template('index.html', features=FEATURE_NAMES)


@app.route('/predict', methods=['POST'])
def predict():
    if model is None:
        return 'Model not found. Run train_model.py first.', 500


    # accept JSON
    if request.is_json:
        data = request.get_json()
        feats = data.get('features')
    else:
        # collect values from named inputs matching FEATURE_NAMES
        feats = []
        for name in FEATURE_NAMES:
            v = request.form.get(name)
            if v is None or v.strip() == '':
                return jsonify({'error': f'Missing feature: {name}'}), 400
            feats.append(v)

    try:
        arr = np.array(feats, dtype=float).reshape(1, -1)
    except Exception as e:
        return jsonify({'error': 'Invalid input features', 'detail': str(e)}), 400

    pred = model.predict(arr)
    proba = None
    if hasattr(model, 'predict_proba'):
        proba = model.predict_proba(arr).tolist()

    return jsonify({'prediction': int(pred[0]), 'probabilities': proba})


if __name__ == '__main__':
    app.run(host='0.0.0.0', port=5000, debug=True)
